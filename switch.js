let teste = 2;
//swtich é uma estrutura de decisão que não trabalha com condições 'true' e 'false', mas ism valores diretos. 
switch (teste){
    case 1: // 
        console.log('Foi de primeira');
        break; 
    case 2: 
        console.log('Foi na segunda');
        break;
    default:
        console.log('Não foi na primeira nem na segunda.')
}