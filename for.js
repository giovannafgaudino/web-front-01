// Comando for é utizada para criar repetições de instruções pelo número
// vezes que precisarmos. 

// na condição for, ler variavel não funciona fora da variável 
for (let i = 0; i<5; i++) {   // let i = 0 ---> inicialização 
    console.log(i);          // i < 5 ---> condição 
}                           // i++ ---> incremento 

